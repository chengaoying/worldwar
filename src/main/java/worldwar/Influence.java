package worldwar;

public class Influence {
	private short id;
	private String name;
	private int color;
	public short getId() {
		return id;
	}
	public void setId(short id) {
		this.id = id;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	
}
